class Command(BaseCommand):
    help = "Print major strain and all files for a predefined list of samples."

    def handle(self, *args, **options):
        for sid in samples:
            sample = (
                Sample.objects
                .filter(pseudonymized_id=sid)
                .select_related("major_strain", "plate")
                .first()
            )

            if not sample:
                logger.warning(f"[MISSING] Sample with pseudonymized_id='{sid}' not found")
                continue

            major_strain_name = sample.major_strain.name if sample.major_strain else "None"
            plate_barcode = sample.plate.barcode if sample.plate else "None"

            logger.info("")  # blank line for readability
            logger.info("=" * 70)
            logger.info(f"Sample: {sid}")
            logger.info(f"  Plate       : {plate_barcode}")
            logger.info(f"  Major strain: {major_strain_name}")

            files = (
                File.objects
                .filter(sample=sample)
                .select_related("type")
                .order_by("path")
            )

            if not files:
                logger.info("  Files:")
                logger.info("    (no files found)")
                logger.info("=" * 70)
                continue

            logger.info("  Files:")
            for f in files:
                postfix = f.type.postfix if f.type else "None"
                logger.info(f"    - path    : {f.path}")
                logger.info(f"      type    : {postfix}")
                logger.info(f"      checksum: {f.checksum}")
            logger.info("=" * 70)